///////////////////// GENERATE RANDOM NUMBERS AND DETERMINE IF RANDOM NUMBER IS EVEN OR ODD.
for (var i = 0; i <= 10; i++) {

    var randNum = Math.floor(Math.random() * (200 - 20)) + 20;

    if (randNum % 2 !== 0) {

        console.log(randNum + " is odd");

    } else {

        console.log(randNum + " is even");

    }
}

