//////////////////////// GENERATE A TIMES TABLE FROM 0 TO 10.
var userNum = prompt("give me a number from 1 to 10");

for (var i = 0; i <= 10; i++) {

    var x = i * userNum;

    console.log(userNum + " x " + i + " = " + x);
}

//////////////////////////////////////////////////////////////////////
//////////////////// FOR LOOP CHALLENGE TWO //////////////////////////
//////////////////////////////////////////////////////////////////////

/////////////////////// GENERATE NUMBER PYRAMID FROM 1 TO 00.
for (i = 1; i <= 10; i++) {

    var a = '';

    for (var n = 1; n <= i; n++) {

        if (i === 10) {

            a = a + '' + (i * 0);

        } else {

            a = a + '' + i;
        }

    }
    console.log(a);

}

